@lru_cache(maxsize=1)
def _load_steam_ksh_rows() -> tuple[tuple[float, tuple[float | None, ...]], ...]:
    rows: list[tuple[float, tuple[float | None, ...]]] = []
    for line in _STEAM_KSH_TABLE_SI.splitlines():
        pressure_str, values_str = line.split("|", 1)
        values: list[float | None] = []
        for token in values_str.split(","):
            token = token.strip()
            values.append(None if token == "" else float(token))
        rows.append((float(pressure_str), tuple(values)))
    return tuple(rows)


def _interp_temp_row(values: tuple[float | None, ...], temperature_c: float) -> float:
    valid = [(temp_c_table, value) for temp_c_table, value in zip(STEAM_KSH_TEMPERATURES_C, values) if value is not None]
    if not valid:
        raise ValueError("Steam KSH row has no valid values.")

    if temperature_c <= valid[0][0]:
        return valid[0][1]
    if temperature_c >= valid[-1][0]:
        return valid[-1][1]

    for (t0, v0), (t1, v1) in zip(valid, valid[1:]):
        if t0 <= temperature_c <= t1:
            frac = (temperature_c - t0) / max(t1 - t0, 1e-12)
            return v0 + frac * (v1 - v0)
    return valid[-1][1]


def lookup_superheat_correction_factor_si(pressure_mpa: float, temperature_c: float) -> float | None:
    rows = _load_steam_ksh_rows()
    pressures = [pressure for pressure, _ in rows]
    if pressure_mpa < pressures[0] or pressure_mpa > pressures[-1]:
        return None
    if temperature_c < STEAM_KSH_TEMPERATURES_C[0] or temperature_c > STEAM_KSH_TEMPERATURES_C[-1]:
        return None

    for pressure, values in rows:
        if abs(pressure_mpa - pressure) < 1e-12:
            return _interp_temp_row(values, temperature_c)

    for (p0, row0), (p1, row1) in zip(rows, rows[1:]):
        if p0 <= pressure_mpa <= p1:
            k0 = _interp_temp_row(row0, temperature_c)
            k1 = _interp_temp_row(row1, temperature_c)
            frac = (pressure_mpa - p0) / max(p1 - p0, 1e-12)
            return k0 + frac * (k1 - k0)
    return None
